/*
Operadores relacionales 

    Mayor que: a>b                       a es mayor que b
    Menor que: a<b                       a es menor que b
    Mayor o igual que: a>=b              a es mayor o igual que b
    Menor o igual que: a<=b              a es menor o igual que b
    Nota: un solo "=" se utiliza cuando estamos asignando valores
    Igual que:  ==   a==b                a es igual a b
    Distinto que:  !=   a!=b             a es diferente de b
    Operador estricto: ===
    Operador diferente estricto: !==
*/

let a = 22;
let b = 15;

// Forma 1
console.log(a > b);
// Forma 2
console.log("Mayor que: " + (a > b));
// Forma 3
console.log("Mayor que: ", a > b);
// Forma 1
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a == b);
console.log(a != b);
